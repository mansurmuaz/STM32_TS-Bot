/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_TIM1_Init();
  MX_TIM6_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
	EASYMX_BOARD_TOUCH_Init(&hadc1, &hadc2, &htim6);
	EASYMX_BOARD_TOUCH_Start();
	HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_4);
	ILI9341_Init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
//	ILI9341_Set_Rotation(SCREEN_HORIZONTAL_1);
//	ILI9341_Fill_Screen(BLUE);

//	uint8_t pDataTransmit[] = "Hello _\r\n";
	uint8_t pDataReceivedByte[] = "_";

		
		for( int i = 0; i < 100; i++ ){
			path[i] = 0;
		}
		
	while (1){		
		
			if (mode == 0){
			
				for( int i = 0; i < counter; i++ ){
					int time = 0;
					switch (path[i]) {
						case 1:
								while (time < 10 ) {
									goForward();
									time++;
								}
								HAL_Delay(500);
								break;
						case 2:
								while (time < 10 ) {
									goBackward();
									time++;
								}
								HAL_Delay(500);
								break;
						case 3:
								while (time < 5 ) {
									goLeft();
									time++;
								}
								HAL_Delay(500);
								break;
						case 4:
								while (time < 5 ) {
									goRight();
									time++;
								}
								HAL_Delay(500);
								break;
					}
					HAL_GPIO_WritePin(GPIOD, GPIO_PIN_8, GPIO_PIN_RESET);
					HAL_GPIO_WritePin(GPIOD, GPIO_PIN_9, GPIO_PIN_RESET);
					HAL_GPIO_WritePin(GPIOD, GPIO_PIN_10, GPIO_PIN_RESET);
					HAL_GPIO_WritePin(GPIOD, GPIO_PIN_11, GPIO_PIN_RESET);
					path[i] = 0;
				}
				counter = 0;				
			}		
		
		
		
//		HAL_UART_Transmit_IT(&huart1, pDataTransmit, 9);
		HAL_UART_Receive_IT(&huart1, pDataReceivedByte, 1);
		
		if ( (coor_x >= 1400 && coor_x <= 2400) || pDataReceivedByte[0] == 'w' || pDataReceivedByte[0] == 's' ) {
				
				if ((coor_y >= 300 && coor_y <= 1350) || pDataReceivedByte[0] == 's' ) {
					if(!mode) {
							goBackward();					
						} else {
							path[counter] = 2;
							counter++;
							__HAL_TIM_SET_AUTORELOAD(&htim1, arr);//set the new period
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, (int)arr/2);//keep 50% duty cycle
							HAL_Delay(800);
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
						}
				} else if ( coor_y >= 2800  || pDataReceivedByte[0] == 'w' ) {
						if(!mode) {
							goForward();			
						} else {
							path[counter] = 1;
							counter++;
							__HAL_TIM_SET_AUTORELOAD(&htim1, arr);//set the new period
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, (int)arr/2);//keep 50% duty cycle
							HAL_Delay(800);
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
						}
				}
		}
	
		if ( (coor_y >= 1450 && coor_y <= 2700 ) || pDataReceivedByte[0] == 'a' || pDataReceivedByte[0] == 'd') {
		
				if ((coor_x >= 300 && coor_x <= 1300) || pDataReceivedByte[0] == 'a'  ) {				// TURN LEFT
				
						
						if(!mode) {
							goLeft();
						} else {
							path[counter] = 3;
							counter++;
							__HAL_TIM_SET_AUTORELOAD(&htim1, arr);//set the new period
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, (int)arr/2);//keep 50% duty cycle
							HAL_Delay(800);
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
						}
						
				} else if ( coor_x >= 2500 || pDataReceivedByte[0] == 'd'  ) {
						if(!mode) {
							goRight();
						} else {
							path[counter] = 4;
							counter++;
							__HAL_TIM_SET_AUTORELOAD(&htim1, arr);//set the new period
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, (int)arr/2);//keep 50% duty cycle
							HAL_Delay(800);
							__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
						}
				}
		}
		HAL_Delay(40);
		
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
		pDataReceivedByte[0] = '_';
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

